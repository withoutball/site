    /* ---- Public helpers (attached to window for other modules) ---- */

    function translate(key, lang) {
        var dict = I18N[lang] || I18N.en;
        if (dict && Object.prototype.hasOwnProperty.call(dict, key)) return dict[key];
        if (I18N.en && Object.prototype.hasOwnProperty.call(I18N.en, key)) return I18N.en[key];
        return key;
    }

    function applyI18n(lang) {
        var title = translate('meta.title', lang);
        if (title && title !== 'meta.title') document.title = title;
        var metaDesc = document.querySelector('meta[name="description"]');
        var desc = translate('meta.description', lang);
        if (metaDesc && desc && desc !== 'meta.description') metaDesc.setAttribute('content', desc);

        document.querySelectorAll('[data-i18n]').forEach(function (el) {
            el.textContent = translate(el.dataset.i18n, lang);
        });

        document.querySelectorAll('[data-i18n-attr]').forEach(function (el) {
            var raw = el.dataset.i18nAttr || '';
            raw.split(';').map(function (s) { return s.trim(); }).filter(Boolean).forEach(function (pair) {
                var idx = pair.indexOf(':');
                if (idx === -1) return;
                var attr = pair.slice(0, idx).trim();
                var key = pair.slice(idx + 1).trim();
                if (!attr || !key) return;
                el.setAttribute(attr, translate(key, lang));
            });
        });
    }

